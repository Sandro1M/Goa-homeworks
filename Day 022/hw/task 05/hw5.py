# capitalize - sandro | Sandro  ამ კოდის მეშვეობით პირველი ასო ყოველთვის დიდია დანარჩენი პატარა
name = "sandro"
print(name.capitalize())

# lower-პატარა ასოებით იბეჭდება სტრინგი | Sandro = sandro
name2 = "luka"
print(name2.lower())

# upper-დიდი ასოებით იბეჭდება სტრინგი | Sandro = SANDRO
name3 = "soso"
print(name3.upper())